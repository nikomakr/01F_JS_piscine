// Function that takes two names and logs them with "and" and "!"
function duos(name1, name2) {
  console.log(name1 + " and " + name2 + " !");
}

// Function that takes two names and a task, and logs them with "and" and "!"
function duosWork(name1, name2, task) {
  console.log(name1 + " and " + name2 + " " + task + " !");
}

